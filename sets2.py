# Union

set1 = {1,2,3,4}
set2 = {3,4,5,6}

results = set1.union(set2)
print(results)


# Intersection

set1 = {1,2,3,4}
set2 = {3,4,5,6}

results = set1.intersection(set2)
print(results)


# Difference

set1 = {1,2,3,4}
set2 = {3,4,5,6}

results = set1.difference(set2)
print(results)

# Symmetric Difference

set1 = {1,2,3,4}
set2 = {3,4,5,6}

results = set1.symmetric_difference(set2)
print(results)

